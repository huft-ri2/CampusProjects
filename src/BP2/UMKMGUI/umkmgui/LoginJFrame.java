    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        loginValid = false;
		
		char[] pass = jPasswordField1.getPassword();
		String inputPass = new String(pass);
		if (!jTextField1.getText().equals(uname)) {
			JOptionPane.showMessageDialog(null, "Username yang anda masukkan tidak ditemukan.");
		} else if (!inputPass.equals(password)) {
			JOptionPane.showMessageDialog(null, "Password yang anda masukkan salah.");
		} else {
			JOptionPane.showMessageDialog(null, "Login berhasil.");
			loginValid = true;
			jTextField1.setText("");
			jPasswordField1.setText("");
		}

		if (loginValid) {
			new AppJFrame().setVisible(true);
			setVisible(false);
		}
    }//GEN-LAST:event_jButton1ActionPerformed

