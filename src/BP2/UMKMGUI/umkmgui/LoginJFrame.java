	private void cekSyaratUname() {
        String ketikan = jTextField1.getText();

        unameValid = false;
        if (ketikan.length() == 0) {
            statusUname.setText(" ");
        } else if (ketikan.length() < 8) {
            statusUname.setText("Panjang username minimal 8 karakter");
            statusUname.setForeground(Color.red);
        } else {
			unameValid = true;
			statusUname.setText(" ");
		}
        updateTombolDaftar();
    }
    
    private void cekSyaratPass() {
		passValid = false;
        char[] ketikan = jPasswordField1.getPassword();
        
        if (ketikan.length == 0) {
            statusPass.setText(" ");
        } else if (ketikan.length < 8) {
            statusPass.setText("Panjang password minimal 8 karakter");
            statusPass.setForeground(Color.red);
        } else {
			passValid = true;
			statusPass.setText(" ");
		}
        updateTombolDaftar();
    }
	
	private void updateTombolDaftar() {
		if (unameValid && passValid) {
			jButton1.setEnabled(true);
		} else {
			jButton1.setEnabled(false);
		}
	}

